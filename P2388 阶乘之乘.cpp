void mysolve()
{
	int n;
	cin >> n;
	ll ans = 0;
	for (int j = 1, p = 5; p <= n; p *= 5, ++j)
		{
			int cnt = n / p;
			ans += p * (cnt - 1) * cnt / 2 + (n % p + 1) * (cnt);
		}
	cout << ans << endl;
}

